package com.student.android.eismodel;

public class Leave {

	private String mReportedBy;
	private String mReportedTo;
	private String mType;
	private String mEntitlement;
	private String mFrom;
	private String mTo;
	private String mReason;
	private String mStatus;
	private String mDays;
	

	public String getmDays() {
		return mDays;
	}
	public void setmDays(String mDays) {
		this.mDays = mDays;
	}
	public String getmReportedBy() {
		return mReportedBy;
	}
	public void setmReportedBy(String mReportedBy) {
		this.mReportedBy = mReportedBy;
	}
	public String getmReportedTo() {
		return mReportedTo;
	}
	public void setmReportedTo(String mReportedTo) {
		this.mReportedTo = mReportedTo;
	}
	public String getmType() {
		return mType;
	}
	public void setmType(String mType) {
		this.mType = mType;
	}
	public String getmEntitlement() {
		return mEntitlement;
	}
	public void setmEntitlement(String mEntitlement) {
		this.mEntitlement = mEntitlement;
	}
	public String getmFrom() {
		return mFrom;
	}
	public void setmFrom(String mFrom) {
		this.mFrom = mFrom;
	}
	public String getmTo() {
		return mTo;
	}
	public void setmTo(String mTo) {
		this.mTo = mTo;
	}
	public String getmReason() {
		return mReason;
	}
	public void setmReason(String mReason) {
		this.mReason = mReason;
	}
	public String getmStatus() {
		return mStatus;
	}
	public void setmStatus(String mStatus) {
		this.mStatus = mStatus;
	}

	
}
